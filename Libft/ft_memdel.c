#include "libft.h"

void	ft_memdel(void **ap)
{
	size_t	i;

	i = 0;
	if (!ap)
		return ;
	while (ap[i])
	{
		free(ap[i]);
		i++;
	}
	free(ap);
	ap = NULL;
}
